import UIKit

extension UIColor {
    class var wpiTurquoise: UIColor {
        return UIColor(red:0.03, green:0.34, blue:0.36, alpha:1.0)
    }
    
    class var wpiDarkTurquoise: UIColor {
        return UIColor(red:0.00, green:0.23, blue:0.27, alpha:1.0)
    }

}
